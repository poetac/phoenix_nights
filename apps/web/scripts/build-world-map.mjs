// Generates apps/web/src/lib/worldMap.js — the City Signals world map: pre-projected
// world land outlines (d3 geoNaturalEarth1 over world-atlas countries-110m) plus each
// registry city's projected [x, y]. Mirrors the committed-output, no-runtime-deps
// pattern of usMap.js: the projected SVG paths are baked in, so the app ships no map
// libraries — only this generator needs them.
//
// The generation deps (d3-geo, topojson-client, world-atlas) are NOT in package.json;
// the "Build world map" workflow installs them with `npm i --no-save` and runs this.
// Run locally the same way from apps/web:
//   npm i --no-save d3-geo topojson-client world-atlas && node scripts/build-world-map.mjs

import { geoNaturalEarth1, geoPath, geoGraticule10 } from "d3-geo";
import { feature } from "topojson-client";
import { writeFileSync } from "node:fs";
import { createRequire } from "node:module";

const require = createRequire(import.meta.url);

const W = 960;
const H = 480;
const PAD = 8;

// world-atlas ships a TopoJSON of country polygons at 1:110m.
const topo = require("world-atlas/countries-110m.json");
const land = feature(topo, topo.objects.countries);

// geoNaturalEarth1 is a balanced whole-world projection; fit it to our viewBox.
const projection = geoNaturalEarth1().fitExtent([[PAD, PAD], [W - PAD, H - PAD]], land);
const toPath = geoPath(projection);

// Trim coordinate precision to one decimal to keep the committed file small.
const round1 = (d) => (d ? d.replace(/-?\d+\.\d+/g, (n) => (+n).toFixed(1)) : d);

const countries = land.features.map((f) => toPath(f)).filter(Boolean).map(round1);
const graticule = round1(toPath(geoGraticule10()));

// Project each city from its [lat, lon] registry entry.
const { CITIES } = await import("../src/lib/cities.js");
const cities = {};
for (const c of CITIES) {
  if (!Array.isArray(c.latLon)) continue;
  const [lat, lon] = c.latLon;
  const xy = projection([lon, lat]);
  if (xy) cities[c.id] = [Math.round(xy[0] * 10) / 10, Math.round(xy[1] * 10) / 10];
}

const banner =
  "// AUTO-GENERATED by apps/web/scripts/build-world-map.mjs — do not edit by hand.\n" +
  "// Pre-projected world land outlines (d3 geoNaturalEarth1, world-atlas countries-110m)\n" +
  "// + each City Signals city's projected [x, y]. Regenerate via the \"Build world map\"\n" +
  "// GitHub Action (it installs d3-geo/world-atlas, which need a network install).\n";
const payload = JSON.stringify({ viewBox: `0 0 ${W} ${H}`, graticule, countries, cities });
writeFileSync(new URL("../src/lib/worldMap.js", import.meta.url),
  `${banner}export const WORLD_MAP = ${payload};\n`);

console.log(`world map: ${Object.keys(cities).length} cities, ${countries.length} country paths -> src/lib/worldMap.js`);
